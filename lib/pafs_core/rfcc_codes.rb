module PafsCore
  # RFCC codes
  # AC – Anglian Central
  # AE – Anglian Eastern
  # AN – Anglian Northern
  # NO – Northumbria
  # NW – North West
  # SN – Southern
  # SO – Solent & South Downs
  # SW – Southwest
  # TH – Thames
  # TR – Trent
  # WX – Wessex
  # YO – Yorkshire
  RFCC_CODES = %w[ AC AE AN NO NW SN SO SW TH TR WX YO ].freeze
end
